package org.backmeup.index;

public class IndexManagerCoreException extends RuntimeException {

    private static final long serialVersionUID = 3110030867711670828L;

    public IndexManagerCoreException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public IndexManagerCoreException(String arg0) {
        super(arg0);
    }

    public IndexManagerCoreException(Throwable arg0) {
        super(arg0);
    }

}
